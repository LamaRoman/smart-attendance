$totalReplacements = 0

foreach ($file in $files) {
    if (-not (Test-Path $file)) {
        Write-Host "SKIP: $file not found" -ForegroundColor Yellow
        continue
    }

    $content = [System.IO.File]::ReadAllText($file, [System.Text.Encoding]::UTF8)
    $originalContent = $content
    $fileReplacements = 0

    $sortedKeys = $replacements.Keys | Sort-Object { $_.Length } -Descending

    foreach ($key in $sortedKeys) {
        $value = $replacements[$key]
        $count = ([regex]::Matches($content, [regex]::Escape($key))).Count
        if ($count -gt 0) {
            $content = $content.Replace($key, $value)
            $fileReplacements += $count
        }
    }

    if ($fileReplacements -gt 0) {
        [System.IO.File]::WriteAllText($file, $content, (New-Object System.Text.UTF8Encoding $true))
        Write-Host "FIXED: $file ($fileReplacements replacements)" -ForegroundColor Green
        $totalReplacements += $fileReplacements
    } else {
        Write-Host "OK:    $file (no garbled text found)" -ForegroundColor Cyan
    }
}

Write-Host "`nDone! Total replacements: $totalReplacements" -ForegroundColor Magenta